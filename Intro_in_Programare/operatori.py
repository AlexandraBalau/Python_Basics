'''
aritmetici: +. -, *, /, % (modulo = restul impartirii 10%3 = 1)
de comparare: <, >, ==, !=, >=, <=
logici: and, or, !
'''

a = 3
b = 5
print(a + b)
print(a == b) #a este egal cu b? False
print(a < b and a < 4) #True and True => True
print(a < b and a > 4) #True and False => False
print(a < b or a < 2) #True or False => True

